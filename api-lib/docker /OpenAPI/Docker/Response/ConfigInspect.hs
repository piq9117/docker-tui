data ConfigInspectResponse
    = ConfigInspectResponse200 Config
    | ConfigInspectResponse404 ErrorResponse
    | ConfigInspectResponse500 ErrorResponse
    | ConfigInspectResponse503 ErrorResponse
    deriving (Show)

instance ToResponse ConfigInspectResponse where
    toResponse (ConfigInspectResponse200 x) =
        Network.Wai.responseBuilder Network.HTTP.Types.status200 ([(Network.HTTP.Types.hContentType, "application/json")]) (Data.Aeson.fromEncoding (Data.Aeson.toEncoding x))
    toResponse (ConfigInspectResponse404 x) =
        Network.Wai.responseBuilder Network.HTTP.Types.status404 ([(Network.HTTP.Types.hContentType, "application/json")]) (Data.Aeson.fromEncoding (Data.Aeson.toEncoding x))
    toResponse (ConfigInspectResponse500 x) =
        Network.Wai.responseBuilder Network.HTTP.Types.status500 ([(Network.HTTP.Types.hContentType, "application/json")]) (Data.Aeson.fromEncoding (Data.Aeson.toEncoding x))
    toResponse (ConfigInspectResponse503 x) =
        Network.Wai.responseBuilder Network.HTTP.Types.status503 ([(Network.HTTP.Types.hContentType, "application/json")]) (Data.Aeson.fromEncoding (Data.Aeson.toEncoding x))

instance GHC.Records.HasField "status" ConfigInspectResponse Network.HTTP.Types.Status where
    getField (ConfigInspectResponse200 {}) = Network.HTTP.Types.status200
    getField (ConfigInspectResponse404 {}) = Network.HTTP.Types.status404
    getField (ConfigInspectResponse500 {}) = Network.HTTP.Types.status500
    getField (ConfigInspectResponse503 {}) = Network.HTTP.Types.status503