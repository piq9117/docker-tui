data NodeUpdateResponse
    = NodeUpdateResponse200
    | NodeUpdateResponse400 ErrorResponse
    | NodeUpdateResponse404 ErrorResponse
    | NodeUpdateResponse500 ErrorResponse
    | NodeUpdateResponse503 ErrorResponse
    deriving (Show)

instance ToResponse NodeUpdateResponse where
    toResponse (NodeUpdateResponse200) =
        Network.Wai.responseBuilder Network.HTTP.Types.status200 ([]) mempty
    toResponse (NodeUpdateResponse400 x) =
        Network.Wai.responseBuilder Network.HTTP.Types.status400 ([(Network.HTTP.Types.hContentType, "application/json")]) (Data.Aeson.fromEncoding (Data.Aeson.toEncoding x))
    toResponse (NodeUpdateResponse404 x) =
        Network.Wai.responseBuilder Network.HTTP.Types.status404 ([(Network.HTTP.Types.hContentType, "application/json")]) (Data.Aeson.fromEncoding (Data.Aeson.toEncoding x))
    toResponse (NodeUpdateResponse500 x) =
        Network.Wai.responseBuilder Network.HTTP.Types.status500 ([(Network.HTTP.Types.hContentType, "application/json")]) (Data.Aeson.fromEncoding (Data.Aeson.toEncoding x))
    toResponse (NodeUpdateResponse503 x) =
        Network.Wai.responseBuilder Network.HTTP.Types.status503 ([(Network.HTTP.Types.hContentType, "application/json")]) (Data.Aeson.fromEncoding (Data.Aeson.toEncoding x))

instance GHC.Records.HasField "status" NodeUpdateResponse Network.HTTP.Types.Status where
    getField (NodeUpdateResponse200 {}) = Network.HTTP.Types.status200
    getField (NodeUpdateResponse400 {}) = Network.HTTP.Types.status400
    getField (NodeUpdateResponse404 {}) = Network.HTTP.Types.status404
    getField (NodeUpdateResponse500 {}) = Network.HTTP.Types.status500
    getField (NodeUpdateResponse503 {}) = Network.HTTP.Types.status503